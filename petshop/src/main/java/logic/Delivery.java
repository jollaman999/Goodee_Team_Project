package logic;

public class Delivery {
    private int order_num;
    private int courier;
    private int songjang_num;

    public int getOrder_num() {
        return order_num;
    }

    public void setOrder_num(int order_num) {
        this.order_num = order_num;
    }

    public int getCourier() {
        return courier;
    }

    public void setCourier(int courier) {
        this.courier = courier;
    }

    public int getSongjang_num() {
        return songjang_num;
    }

    public void setSongjang_num(int songjang_num) {
        this.songjang_num = songjang_num;
    }
}
